    private void calculateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calculateButtonActionPerformed

        //Validate form
        validateForm();

        //If the form is properly completed calculate the total
        if (formValidated) {

            //Add selected existing health conditions to an array list
            existingHealthCondition = (ArrayList<String>) existingHealthConditionList.getSelectedValuesList();

            //Create Customer
            customer = new Customer(firstName, lastName, gender, age, smokerStatus, existingHealthCondition, other);

            //Set number format to currency  
            cf = NumberFormat.getCurrencyInstance();

            if (policyTypeComboBox.getSelectedItem().equals("Individual Policy")) {
                policy = new IndividualPolicy(policyType, hospitalCover, dayToDayExpenses, idNumber, formattedNumber);
                policy.setTotal(policy.calculatePremium(customer));
                //  display price on form with euro sign
                displayTotalLabel.setText(String.valueOf(cf.format(policy.getTotal())));
            } else if (policyTypeComboBox.getSelectedItem().equals("Corporate Policy")) {
                policy = new CorporatePolicy(policyType, hospitalCover, dayToDayExpenses, idNumber, formattedNumber);
                policy.setTotal(policy.calculatePremium(customer));
                //  display price on form with euro sign
                displayTotalLabel.setText(String.valueOf(cf.format(policy.getTotal())));
            }

            //  reset form validation 
            formValidated = false;

            clearFormButton.setEnabled(true);
            buyPolicyButton.setEnabled(true);
            calculateButton.setEnabled(false);

            //If age is over 70 disable the buy function and calculate button
            if (age.equals("Over 70")) {
                displayTotalLabel.setText(String.valueOf(cf.format(0)));
                clearFormButton.setEnabled(true);
                buyPolicyButton.setEnabled(false);
                calculateButton.setEnabled(true);

                Component frame = null;
                JOptionPane.showMessageDialog(frame,
                        "No quotes given for over 70's",
                        "Error",
                        JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_calculateButtonActionPerformed

