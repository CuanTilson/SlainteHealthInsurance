    //Method to Validate Form 
    private void validateForm() {
        //Checks if first name is not filled in if not selected -> prompt user to fill in first name 
        if (firstNameTextbox.getText().equals("")) {
            JOptionPane.showMessageDialog(this,
                    "Please input first name",
                    "Warning!", JOptionPane.WARNING_MESSAGE);
        } else if (lastNameTextbox.getText().equals("")) {
            JOptionPane.showMessageDialog(this,
                    "Please input last name",
                    "Warning!", JOptionPane.WARNING_MESSAGE);
        } else if (genderButtonGroup.getSelection() == null) {
            JOptionPane.showMessageDialog(this,
                    "Please select a gender",
                    "Warning!", JOptionPane.WARNING_MESSAGE);
        } else if (ageComboBox.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(this,
                    "Please select an age",
                    "Warning!", JOptionPane.WARNING_MESSAGE);
        } else if (policyTypeComboBox.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(this,
                    "Please select a policy type",
                    "Warning!", JOptionPane.WARNING_MESSAGE);
        } else if (hospitalCoverComboBox.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(this,
                    "Please select a hospital cover",
                    "Warning!", JOptionPane.WARNING_MESSAGE);
        } else if (dayToDayExpensesComboBox.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(this,
                    "Please select a day to daye expenses type",
                    "Warning!", JOptionPane.WARNING_MESSAGE);
        } else if (existingHealthConditionList.isSelectionEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Please select an option from the existing health conditions list",
                    "Warning!", JOptionPane.WARNING_MESSAGE);
        } else if (smokerButtonGroup.getSelection() == null) {
            JOptionPane.showMessageDialog(this,
                    "Please idicate whether or not you are a smoker",
                    "Warning!", JOptionPane.WARNING_MESSAGE);
        } else {
            // Asumme form completed correctly 
            formValidated = true;
        }
    } //end validate form method

